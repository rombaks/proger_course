class HomeNews(MyMixin, ListView):
    model = News  # заменяет news = News.objects.all() из index(). ссылка на шаблон $имя_модели$_list.html
    template_name = 'news/home_news_list.html'  # замена стандартного news_list.html
    context_object_name = 'news'  # замена стандартного object_list
    # extra_context = {'title': 'Главная'}
    mixin_prop = 'hello world'
    paginate_by = 2

    def get_context_data(self, **kwargs):
        """ Возвращает контекст, в тч динамический """
        context = super().get_context_data(**kwargs)
        context['title'] = self.get_upper('Главная страница')
        context['mixin_prop'] = self.get_prop()
        return context

    def get_queryset(self):
        """ Формирует запрос. В данном примере чтобы отображались только новости is_published=True"""
        return News.objects.filter(is_published=True).select_related('category')


class NewsByCategory(MyMixin, ListView):
    model = News
    template_name = 'news/home_news_list.html'
    context_object_name = 'news'
    allow_empty = False
    paginate_by = 2

    def get_context_data(self, **kwargs):
        """ Возвращает контекст, в тч динамический """
        context = super().get_context_data(**kwargs)
        context['title'] = self.get_upper(Category.objects.get(pk=self.kwargs['category_id']))
        return context

    def get_queryset(self):
        return News.objects.filter(category_id=self.kwargs['category_id'],
                                   is_published=True).select_related('category')


